#!/usr/bin/env node
/**
 * Extract every <style> block from the v6 mockup into a single mockup.css
 * file. Preserves the order — order matters because the mockup uses cascading
 * overrides (v3-library-rooms is amended by v4-polish-overrides etc.).
 *
 * Why dump it all rather than cherry-pick:
 * - Pixel parity is the success criterion.
 * - The mockup CSS is 5000+ lines built across many iterations; cherry-picking
 *   risks subtle differences.
 * - The class names are stable (.app, .sidebar, .sb-item, .pane, etc.) — as
 *   long as the Astro HTML preserves them, the CSS applies.
 *
 * Output: src/styles/mockup.css
 */

import { readFileSync, writeFileSync } from 'node:fs';
import { join, resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(__dirname, '..');
const MOCKUP = '/Users/seanmelis/Documents/_cowork/outputs/website-rebrand/melis-ai-spa-v6-astro-content.html';
const OUT = join(repoRoot, 'src', 'styles', 'mockup.css');

const html = readFileSync(MOCKUP, 'utf8');

// Match every <style ...>…</style> block (greedy across newlines)
const re = /<style[^>]*>([\s\S]*?)<\/style>/g;
const blocks = [];
let m;
let i = 0;
while ((m = re.exec(html)) !== null) {
  i++;
  // Try to capture the id attribute for a labelled comment
  const opener = html.slice(m.index, m.index + (m[0].indexOf('>') + 1));
  const idMatch = /id="([^"]+)"/.exec(opener);
  const label = idMatch ? idMatch[1] : `block-${i}`;
  blocks.push({ label, css: m[1] });
}

const banner = `/* ──────────────────────────────────────────────────────────────
 * mockup.css — verbatim CSS lifted from melis-ai-spa-v6.
 * Auto-generated by scripts/extract-mockup-css.mjs. Do not edit by hand;
 * regenerate from the mockup if the design needs to change.
 *
 * The mockup is the source of truth for visual design.
 * ──────────────────────────────────────────────────────────────
 */
`;

const out =
  banner +
  blocks
    .map((b) => `\n/* ──────── ${b.label} ──────── */\n${b.css.trim()}\n`)
    .join('\n');

writeFileSync(OUT, out);
console.log(`Wrote ${blocks.length} style blocks (${(out.length / 1024).toFixed(1)} KB) to ${OUT}`);
